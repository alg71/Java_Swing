    private void jButtonConfirmarReservaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonConfirmarReservaActionPerformed

        String mensaje = "¿Desea confirmar la reserva?";
        int respuesta = JOptionPane.showConfirmDialog(this, mensaje, "Confirmación", JOptionPane.YES_NO_OPTION);

        if (respuesta == JOptionPane.YES_OPTION) { // el método JOptionPane.YES_OPTION devulve un cero.
            // Cierra el JDialog actual
            this.dispose();

            nombre = jTextFieldNombre.getText();
            apellidos = jTextFieldApellidos.getText();
            tipoEvento = jComboBoxTipoevento.getSelectedItem().toString();
            numeroDias = (int) jSpinnerDiasCongreso.getValue();
            numeroHabitaciones = (int) jSpinnerNumeroHabitaciones.getValue();
            
            // Se formatea fecha con la clase SimpleDateFormat para mostrar formato dd/mm/aa
            Date fecha = (Date) jSpinnerFechaEvento.getValue();
            SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
            fechaEvento = sdf.format(fecha);

            tipoCocina = jComboBoxTipoCocina.getSelectedItem().toString();
            
            // llamada al método de la clase principal y pasar parámetros
            pantallaPrincipal.mostrarResumenReserva(nombre, apellidos, tipoEvento, numeroDias, numeroHabitaciones, fechaEvento, tipoCocina);

            // Volvemos a la pantalla principal (JFrame pantallaPrincipal)
            pantallaPrincipal.setVisible(true);
        }


    }//GEN-LAST:event_jButtonConfirmarReservaActionPerformed

