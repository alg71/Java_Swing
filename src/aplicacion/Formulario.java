    private void jComboBoxTipoeventoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBoxTipoeventoActionPerformed

        String seleccion = (String) jComboBoxTipoevento.getSelectedItem();

        // Activa o desactiva los siguientes componentes si se elige opción "Congreso"
        if ("Congreso".equals(seleccion)) {
            // Habilita los componentes relacionados con la elección de "Congreso"
            jCheckBoxHabitaciones.setEnabled(true);
            jLabelDuracionCongreso.setEnabled(true);
            jSpinnerDiasCongreso.setEnabled(true);
        } else {
            // Deshabilita los componentes si no es un "Congreso"
            jCheckBoxHabitaciones.setEnabled(false);
            jLabelDuracionCongreso.setEnabled(false);
            jSpinnerDiasCongreso.setEnabled(false);
        }
    }//GEN-LAST:event_jComboBoxTipoeventoActionPerformed

